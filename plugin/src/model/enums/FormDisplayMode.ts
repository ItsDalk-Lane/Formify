/**
 * 多表单界面显示模式
 * 用于配置多个"调用表单"动作的界面显示方式
 */
export enum FormDisplayMode {
    /**
     * 逐个打开（默认）
     * 第一个表单打开 → 用户填写并提交 → 第一个表单关闭 → 第二个表单打开 → ...
     */
    SINGLE = "single",
    
    /**
     * 合并显示
     * 所有表单的字段合并到一个模态框中显示，每个表单的字段区域有清晰的标题或标识区分
     */
    MERGED = "merged",
}
